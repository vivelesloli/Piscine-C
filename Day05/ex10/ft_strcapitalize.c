char	*ft_strcapitalize(char *str)
{
	int i;

	i = -1;
	while (str[++i])
	{
		if (str[0] <= 'z' && str[0] >= 'a')
		{
			str[0] = str[0] - 32;
		}
		else if ((str[i] == '-' || str[i] == ' ' || str[i] == '+')
				&& (str[i + 1] >= 'a' && str[i + 1] <= 'z'))
		{
			str[i + 1] = str[i + 1] - 32;
		}
		else if (((str[i] >= 'A' && str[i] <= 'Z' && i > 0)
					&& (str[i - 1] >= 'A' && str[i - 1] <= 'Z'))
				|| ((str[i] >= 'A' && str[i] <= 'Z' && i > 0)
					&& (str[i - 1] >= 'a') && str[i - 1] <= 'z'))
		{
			str[i] = str[i] + 32;
		}
	}
	return (str);
}
