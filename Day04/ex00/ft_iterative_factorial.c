int		ft_iterative_factorial(int nb)
{
	int n;

	n = nb;
	if (n < 0 || n > 12)
		return (0);
	if (nb == 0)
		return (1);
	else
	{
		while (n > 1)
		{
			nb *= (n - 1);
			n--;
		}
	}
	return (nb);
}
